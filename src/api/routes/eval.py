"""
GET /eval-report — serve the committed evaluation report.

Returns the contents of reports/eval-report.json, which is generated by
scripts/evaluate.py after training. Committed to the repo so the eval
dashboard works without re-running training.

Returns 404 with a clear message if the eval report has not yet been
generated (i.e., Gate 3 has not been run yet).
"""

from pathlib import Path

from fastapi import APIRouter, HTTPException
from fastapi.responses import JSONResponse

router = APIRouter()

REPORTS_DIR = Path(__file__).parent.parent.parent.parent / "reports"
EVAL_REPORT_PATH = REPORTS_DIR / "eval-report.json"


@router.get("/eval-report")
async def get_eval_report():
    """
    Return the committed evaluation report from reports/eval-report.json.

    Includes AUROC, F1 at three operating thresholds, calibration metrics,
    SHAP global summary reference, latency benchmarks, and ROI cost model.

    Run scripts/evaluate.py (Gate 3) to generate this report after training.
    """
    if not EVAL_REPORT_PATH.exists():
        raise HTTPException(
            status_code=404,
            detail=(
                "Evaluation report not found. "
                "Run: uv run python scripts/evaluate.py\n"
                "Expected output: reports/eval-report.json, reports/shap-summary.png, "
                "reports/roc-curve.png, reports/calibration-plot.png"
            ),
        )

    import json

    with open(EVAL_REPORT_PATH) as f:
        report = json.load(f)

    return JSONResponse(content=report)
